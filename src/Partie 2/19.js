// Ajouter et supprimer des éléments
// Manipulez un tableau de langages en utilisant les méthodes push(), pop() et unshift()
// 1. Création du tableau initial
let langages = ["JavaScript", "Python", "Java"];
console.log("Initial:", langages);

// 3. Ajouter à la fin
langages.push("Go");
console.log("Après push:", langages);

// 5. Enlever le dernier élément
langages.pop();
console.log("Après pop:", langages);

// 7. Ajouter au début
langages.unshift("TypeScript");
console.log("Après unshift:", langages);