// Initialisation du compteur à 10
let compteur = 10;

// Boucle while qui s'exécute tant que le compteur est supérieur ou égal à 0
while (compteur >= 0) {
    console.log(compteur); // Affiche la valeur actuelle
    compteur--; // Décrémente le compteur pour éviter une boucle infinie
}

// Affiche le message de fin une fois la boucle terminée
console.log('Décollage !');